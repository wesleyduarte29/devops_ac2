class Operacoes():
    def soma(self, valores):
        val = 0
        for v in valores:
            val = val + v
        return val

    def subtracao(self, valores):
        val = 0
        for v in valores:
            val = val - v
        return val

    def mult(self, valores):
        val = 2
        for v in valores:
            val = val * v
        return val
